STSG SEGMENT PARA STACK 'STSG'
	DW 32 DUP(?)
STSG ENDS

DTSG SEGMENT PARA 'DTSG'
	N DW 500
	ARRAY DW 1, 2, 8, 20, 25, 495 DUP(81)
	ODD DW 0
DTSG ENDS

CDSG SEGMENT PARA 'CDSG'
	ASSUME SS:STSG, DS:DTSG, CS:CDSG
	
	MAIN PROC FAR
		PUSH DS
		XOR AX, AX
		PUSH AX
		
		MOV AX, DTSG
		MOV DS, AX
		
		MOV CX, N
		XOR SI, SI
COUNT:
		MOV AX, ARRAY[SI]
		SHR AX, 1
		ADC ODD, 0
		LOOP COUNT
		
		MOV AX, ODD
		
		RETF
	MAIN ENDP
CDSG ENDS
	END MAIN