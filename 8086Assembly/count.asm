PUBLIC COUNT

CDSG SEGMENT PARA PUBLIC 'CDSG'
	ASSUME CS:CDSG
	COUNT PROC FAR
		PUSH BP
		MOV BP, SP
		PUSH BX
		PUSH CX
		PUSH DX
		
		MOV DX, [BP + 10]
		MOV BX, [BP + 8]
		MOV CX, [BP + 6]
		
		XOR AX, AX
.L1:
		CMP DX, [BX]
		JNE .L2
		INC AX
.L2:
		ADD BX, 2
		LOOP .L1
		
		POP DX
		POP CX
		POP BX
		POP BP
		RETF 6
	COUNT ENDP
CDSG ENDS
	END