STSG SEGMENT PARA STACK 'STSG'
	DW 32 DUP(?)
STSG ENDS

DTSG SEGMENT PARA 'DTSG'
	NUMA DW 0F000H
	NUMB DW 0E000H
	RES DD 0
DTSG ENDS

CDSG SEGMENT PARA 'CDSG'
	ASSUME SS:STSG, DS:DTSG, CS:CDSG
	
	MAIN PROC FAR
		PUSH DS
		XOR AX, AX
		PUSH AX
		
		MOV AX, DTSG
		MOV DS, AX
		
		LEA BX, RES
		MOV AX, NUMA
		ADD AX, NUMB
		
		MOV [BX], AX
		ADC WORD PTR [BX + 2], 0

		RETF
	MAIN ENDP
CDSG ENDS
	END MAIN