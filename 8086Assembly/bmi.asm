STSG SEGMENT PARA STACK 'STSG'
	DW 32 DUP(?)
STSG ENDS

DTSG SEGMENT PARA 'DTSG'
	N DW 5
	MASS DW 265, 186, 220, 195, 280
	HEIGHT DB 59, 72, 77, 54, 61
	BMI DW 5 DUP(0)
DTSG ENDS

CDSG SEGMENT PARA 'CDSG'
	ASSUME SS:STSG, DS:DTSG, CS:CDSG
	
	MAIN PROC FAR
		PUSH DS
		XOR AX, AX
		PUSH AX
		
		MOV AX, DTSG
		MOV DS, AX
		
		; BMI = MASS * 703 / (HEIGHT)^2
		
		MOV CX, N
		XOR SI, SI
		MOV DI, 703
.L1:
		XOR AX, AX
		MOV AL, HEIGHT[SI]
		MUL AL
		MOV BX, AX	;	BX <- W * 703
		SHL SI, 1
		MOV AX, MASS[SI]
		MUL DI
		DIV BX
		MOV BMI[SI], AX
		SHR SI, 1
		INC SI
		LOOP .L1

		RETF
	MAIN ENDP
CDSG ENDS
	END MAIN