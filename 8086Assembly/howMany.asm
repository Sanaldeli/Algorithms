EXTRN COUNT:FAR

STSG SEGMENT PARA STACK 'STSG'
	DW 32 DUP(?)
STSG ENDS

DTSG SEGMENT PARA 'DTSG'
	ARRAY DW 12, 12, 12, 12, 12, 12, 13, 14, 15, 16, 17
	N DW 11
	SEARCHED DB 15
	COUNTER DW 0
DTSG ENDS

CDSG SEGMENT PARA PUBLIC 'CDSG'
	ASSUME SS:STSG, DS:DTSG, CS:CDSG
	
	MAIN PROC FAR
		PUSH DS
		XOR AX, AX
		PUSH AX
		
		MOV AX, DTSG
		MOV DS, AX
		
		XOR AX, AX
		MOV AL, SEARCHED
		PUSH AX
		LEA AX, ARRAY
		PUSH AX
		PUSH N
		CALL COUNT
		MOV COUNTER, AX

		RETF
	MAIN ENDP
CDSG ENDS
	END MAIN